# A program that checks whether a pH level is basic, acidic, or neutral.

ph = 10
if ph > 7:
  print ('Basic')
elif ph < 7:
  print ('Acidic')
else:
  print ('Neutral')

# Elif : One or more elif statements (short for "else if") can be optionally added in between the if and else to provide additional condition(s) to check. Sometimes two is simply not enough.
